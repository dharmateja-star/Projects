class Switch
{
	public static void main(String[] args) 
	{
		int num=200;

		switch(num)
		{

			case 100: System.out.println("Case 1 satisfies");
			break;

			case 300: System.out.println("Case 2 satisfies");
			break;

			case 200: System.out.println("Case 3 satisfies");
			break;

			default : System.out.println("No case is matching");
			
		}
	}
}
