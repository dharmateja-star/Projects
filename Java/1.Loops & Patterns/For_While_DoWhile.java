 
class For_While_DoWhile
{
	public static void main(String[] args) 
	{
		System.out.println("For Loop");
		for(int i=0;i<=4;i++)
		{
			System.out.println("*");
			
		}


		System.out.println("While Loop");
		int j=0;
		while(j<5)
		{
			System.out.println("*");
			j++;
		}


		System.out.println("DoWhile Loop");
		int k=5;
		do{
			System.out.println("*");
			k++;
		}while(k<10);

		
		
		
	}
}
